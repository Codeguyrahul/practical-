package Array;

public class Array_2d {
	public static void main(String[] args) {
		// declaring & initializing 2D Array
		int arr[][]= {{2,5,6},{4,8,9}};
		for(int i=0; i<2;i++) {
			for(int j=0;j<3;j++) {
				System.out.print(arr[i][j]+" ");
			}
			System.out.println();
		}
	}
	

}
