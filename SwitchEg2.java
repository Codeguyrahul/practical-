package ControlStatement;

public class SwitchEg2 {
public static void main(String[] args) {
	char ch='a';
	switch(ch) {
	case'd':
		System.out.println("first case excute");
		break;
	case'e':
		System.out.println("second  case excute");
		break;
	case'a':
		System.out.println("thrid case excute");
		break;
	case'g':
		System.out.println("fourth case excute");
		break;
		default:System.out.println("ivaild");
	}
}
}
